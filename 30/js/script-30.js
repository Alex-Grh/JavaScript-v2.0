// Очень часто рекурсию применяют для анимации
// Есть <div class="test-2 red"></div> пропишем его движения без рекурсии

let offset = 0;

function move() {
   offset = offset +5;
   document.querySelector('.test-2').style.left = offset + 'px';
   // добавим рекурсию необходимо чтобы функция постоянно вызывала сама себя
   if (offset > 300) { // добавляем ограничитель чтобы небыло безконечного цикла
      return true;
      
   }
   //добавим задержку по времени setTimeout(функция move, задержка в милисек 500)
   setTimeout(move, 500)
   // move();
}
document.querySelector('button').onclick = move;