// Рекурсия это возможность функции вызвать саму себя
// Чтобы выйти с рекурсии необходимо использовать if (a > 10) return a; Ставить огранечитель или будет завысания

//безконечный цыкл
let a = 1;
function rec() {
   a++;
   console.log(a);
   if (a > 10) {
      return a; // Останавливаем функцию и возвращаем последние значение
      //Оператор **return **завершает выполнение текущей функции и возвращает её значение.
   }
   rec();
}
rec();

